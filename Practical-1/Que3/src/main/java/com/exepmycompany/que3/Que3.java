package com.exepmycompany.que3;
public class Que3 
{
    public static void main(String[] args) 
    {
        int i;
        for(i=0;i<=4;i++)
        {
            System.out.println("Executing loop "+i);
        }
        while(i<=4)
        {
            System.out.println("Executing loop "+i);
            i++;
        }
    }
}
